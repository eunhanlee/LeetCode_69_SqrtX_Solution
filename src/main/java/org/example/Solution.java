package org.example;


public class Solution {
    /**
     * Calculates and returns the integer square root of the given non-negative integer x.
     *
     * @param x The non-negative integer x for which to calculate the square root.
     * @return The largest integer whose square is less than or equal to x.
     */
    public int mySqrt(int x) {
        return (int) Math.sqrt(x);
    }
}
